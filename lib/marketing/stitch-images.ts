export type StitchImageQuality = "max" | "ultraWide" | "background" | "avatar";

/**
 * Build a Google usercontent CDN URL.
 * Avoid =s0 (original max) — multi‑MB files cause /_next/image upstream timeouts.
 */
export function stitchImageUrl(
  url: string,
  quality: StitchImageQuality = "background"
): string {
  const base = url.split("=")[0]!;
  switch (quality) {
    case "max":
      return `${base}=w2560-rw`;
    case "ultraWide":
      return `${base}=w3840-rw`;
    case "background":
      return `${base}=w1920-rw`;
    case "avatar":
      return `${base}=w256-rw`;
  }
}

